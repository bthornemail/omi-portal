export function getSolid(name) {
  const solid = PLATONIC_SOLIDS[name];
  if (!solid) throw new RangeError(`Unknown platonic solid: ${name}. Choose from: ${Object.keys(PLATONIC_SOLIDS).join(", ")}`);
  return solid;
}

export function peerList(name, vertexIndex) {
  const solid = getSolid(name);
  const peers = new Set();
  for (const [a, b] of solid.edges) {
    if (a === vertexIndex) peers.add(b);
    if (b === vertexIndex) peers.add(a);
  }
  return [...peers].sort((x, y) => x - y);
}
